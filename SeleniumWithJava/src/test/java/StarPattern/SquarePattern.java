package StarPattern;

public class SquarePattern 
{

	 public static void main(String[] args) 
 {

	// size of the square
	  int size = 5;
   // outer loop
	 for (int i = 0; i < size; i++) 
	 {
		 // inner loop
		 for (int j = 0; j < size; j++) 
		 {
			 System.out.print("*");
		 }
	 System.out.println();
	 }
 }
		
}
